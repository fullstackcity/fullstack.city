                    <h2>3. Website Maintenance</h2>
                    <h4>Owning a website is more than just uploading files and forgetting they exist. Times change and so must a website. Up coming events must be posted if a business is to thrive in the long run. One way to keep customers coming back is to post content regularly. And much like social media sites, a website must change with the times and with what’s going on in it's potential industry. Also, if a site features a blog, it requires uploads, changes, edits, and comments...website maintenance. Further, <a href="https://www.shoutmeloud.com/blog-comments-effect-search-engine-ranking.html" target="_blank">commenting on a website's blog is highly effective</a>
                        when it comes to seo. <br><br>See, it’s more than just uploading and forgetting. It’s about engagement. Engagement with customers and clients and users. Users want to know that businesses care about the mix. They want to feel they are apart of something--something big--no one wants to feel left out. Even more, if you upload regularly and engage with your users, your site has <a href="https://seositecheckup.com/articles/blog-commenting-in-2015-making-one-of-the-oldest-seo-strategies-pay-off-big-time">tremendous potential to rank well with Search Engines</a>.</h4>
