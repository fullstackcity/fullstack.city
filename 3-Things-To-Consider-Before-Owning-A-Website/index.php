                        <h4><strong>Lastly,</strong> your domain name will cost, generally, anywhere between $10 - $30 or more a year depending on the name. Keep in mind too that there is a wide selection of <a href="https://iwantmyname.com/domains/domain-name-registration-list-of-extensions" target="_blank">domain extensions</a> to choose from. Such as,
                            <a href="https://en.wikipedia.org/wiki/.net" target="_blank">.net</a> <a href="https://en.wikipedia.org/wiki/.shop" target="_blank">.shop</a> <a href="https://en.wikipedia.org/wiki/.tv" target="_blank">.tv</a> or <a href="https://en.wikipedia.org/wiki/.us" target="_blank">.us</a>, to name a few. And no need to feel bad if the .com extension isn't available. In fact, there are some pretty cool ones out there to choose from. Check this out: <a href="https://iwantmyname.com/domains/new-gtld-domain-extensions" target="_blank">...Here</a>.
